export function triviaLineForJapaneseHoliday(holidayNameJa: string): string | null {
  const key = (holidayNameJa ?? "").trim();
  if (!key) return null;
  const direct = FACTS[key];
  if (direct) return direct;
  const noParen = key.replace(/（[^）]*）\s*$/u, "").trim();
  if (noParen !== key && FACTS[noParen]) return FACTS[noParen]!;
  const head = key.split(/[・／]/u)[0]?.trim() ?? "";
  if (head && head !== key && FACTS[head]) return FACTS[head]!;
  return null;
}
